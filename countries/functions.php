function generateFolderName($txt, $country)
{
    $folderName = transliterateString($txt);
    $folderName = str_replace(' ', '-', strtolower($folderName));
    $folderName = str_replace('\'', '-', $folderName);
    $folderName = str_replace('\\', '', $folderName);
    $folderName = str_replace('.', '-', $folderName);
    $folderName = str_replace('--', '-', $folderName);

    if ($country == 'France')
    {
        $folderName = str_replace('&', 'et', $folderName);
    }
    else
    {
        $folderName = str_replace('&', 'and', $folderName);
    }

    return $folderName;
}
